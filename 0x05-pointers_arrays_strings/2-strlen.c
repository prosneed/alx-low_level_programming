/**
 * _strlen - displays the length of a string
 * @s: a string pointer to a char
 * Return: 0 if successful
 */

int _strlen(char *s)
{
	int j, length = 0;

	for (i = 0; i != '\0'; i++)
	{
		length++;
	}

	for (j = 0; j != '\0'; j++)
	{
		s[j];
		length++;
	}
	return (length);
}
